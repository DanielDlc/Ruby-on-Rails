# instrução que repete um bloco de código enquanto sua condição for verdadeira
# enquanto todas as condições forem verdadeiras ela fica no loop e irá parar quando a condição for falsa.

# contador de 1 até 10
contador = 1

while contador <= 10
    puts contador

    contador += 1  # enquanto o contador for menor que 10, ele adiciona um número.
end